namespace Tereta\Import\Model\Core\Scope;

use Magento\Framework\DataObject;
use Tereta\Import\Model\Logger;
use Tereta\Import\Model\ResourceModel\Core\Scope\Stock as StockResource;
use Magento\InventoryIndexer\Indexer\InventoryIndexer;
use Magento\CatalogInventory\Model\Indexer\Stock\Processor as StockIndexer;

/**
 * Tereta\Import\Model\Core\Scope\Stock
 *
 * Class Stock
 * @package Tereta\Import\Model\Core\Scope
 * @author Tereta Alexander <tereta@mail.ua>
 */
class Stock extends AbstractModel
{
    /**
     * @var DataObject
     */
    protected $configuration;

    /**
     * @var array
     */
    protected $_stockDataRecords = [];

    /**
     * @var array
     */
    protected $_stockItemDataRecords = [];

    /**
     * @var Logger
     */
    protected $logger;

    /**
     * @return array
     */
    public function getSkipAttributes(): array
    {
        return [
            'is_in_stock',
            'qty',
            'manage_stock'
        ];
    }

    /**
     * @return array
     */
    public function getIncludeAttributes(): array
    {
        return ['quantity_and_stock_status'];
    }

    /**
     *
     */
    protected function _construct(): void
    {
        $this->_init(StockResource::class);
        $this->getResource()->setConfiguration($this->configuration)->setLogger($this->logger);
    }

    /**
     *
     */
    public function save(): void
    {
        $this->getResource()->saveStock();
    }

    /**
     * @param array $data
     */
    public function collect(array &$data): void
    {
        // FROM CONFIGURATION
        if (!isset($data['manage_stock']) && $this->configuration->getData('manage_stock') == 1) {
            $data['manage_stock'] = true;
        }

        if (!isset($data['manage_stock']) && $this->configuration->getData('manage_stock') == 2) {
            $data['manage_stock'] = false;
        }

        if (!isset($data['is_in_stock']) && $this->configuration->getData('is_in_stock') == 1) {
            $data['is_in_stock'] = true;
        }

        if (!isset($data['is_in_stock']) && $this->configuration->getData('is_in_stock') == 2) {
            $data['is_in_stock'] = false;
        }

        if (isset($data['manage_stock'])) {
            $data['quantity_and_stock_status'] = $data['manage_stock'];
        }

        $this->getResource()->collect($data);
    }

    /**
     * @param DataObject $skuEntities
     */
    public function fillEntityIds(DataObject $skuEntities): void
    {
        $this->getResource()->fillEntityIds($skuEntities);
    }

    /**
     *
     */
    public function getIndexer(): array
    {
        return [
            InventoryIndexer::INDEXER_ID, StockIndexer::INDEXER_ID
        ];
    }
}
