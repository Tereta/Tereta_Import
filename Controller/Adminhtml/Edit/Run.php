namespace Tereta\Import\Controller\Adminhtml\Edit;

use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;
use Tereta\Import\Model\ImportFactory;
use Magento\Framework\View\Result\PageFactory;
use Magento\Framework\Controller\AbstractResult;
use Exception;

/**
 * Tereta\Import\Controller\Adminhtml\Edit\Run
 *
 * Class Run
 * @package Tereta\Import\Controller\Adminhtml\Edit
 * @author Tereta Alexander <tereta@mail.ua>
 */
class Run extends Action
{
    /**
     * @var ImportFactory
     */
    protected $importFactory;

    /**
     * Run constructor.
     * @param PageFactory $resultPageFactory
     * @param ImportFactory $importFactory
     * @param Context $context
     */
    public function __construct(ImportFactory $importFactory, Context $context)
    {
        $this->importFactory = $importFactory;
        parent::__construct($context);
    }

    /**
     * @return bool
     */
    protected function _isAllowed(): bool
    {
        return $this->_authorization->isAllowed('Tereta_Import::import');
    }

    /**
     * @return string
     */
    public function execute(): AbstractResult
    {
        $resultRedirect = $this->resultRedirectFactory->create();
        $importId = $this->getRequest()->getParam('import_id');
        $importModel = $this->importFactory->create();
        $startTime = time();

        try {
            $importModel->importById($importId);
            $this->messageManager->addSuccessMessage(
                __(
                    'Imported successfully in %1sec.',
                    (time() - $startTime)
                )
            );
        } catch (Exception $ex) {
            $this->messageManager->addErrorMessage($ex->getMessage());
        }

        return $resultRedirect->setPath('advencedimport/edit/index', ['entity_id' => $importId]);
    }
}
