namespace Tereta\Import\Controller\Adminhtml\Edit;

use Magento\Backend\App\Action;
use Magento\Backend\App\Action\Context;
use Magento\Framework\Controller\AbstractResult;
use Magento\Framework\Controller\ResultFactory;
use Tereta\Import\Helper\Data as HelperData;
use Tereta\Import\Model\Import\Repository as ImportRepository;
use Exception;

/**
 * Class File
 * @package Controller\Adminhtml\Edit
 */
class Log extends Action
{
    protected $helperData;

    protected $importRepository;

    public function __construct(ImportRepository $importRepository, Context $context, HelperData $helperData)
    {
        $this->importRepository = $importRepository;
        $this->helperData = $helperData;
        parent::__construct($context);
    }

    /**
     * @return bool
     */
    protected function _isAllowed(): bool
    {
        return $this->_authorization->isAllowed('Tereta_Import::import');
    }

    /**
     * @return AbstractResult
     */
    public function execute()
    {
        $entityId = $this->getRequest()->getParam('entity_id');
        $logType = $this->getRequest()->getParam('type');
        try {
            switch ($logType) {
                case ('skip'):
                    $contentType = 'text/csv';
                    $content = $this->getSkippedCsv($entityId);
                    break;
                default:
                    throw new Exception(__('Type was not found'));
                    break;
            }

            $result = $this->resultFactory->create(ResultFactory::TYPE_RAW);
            return $result->setHeader('Content-Type', $contentType)->setContents($content);
        } catch (Exception $e) {
            $this->messageManager->addErrorMessage(__('The file can not be downloaded'));
            $resultRedirect = $this->resultRedirectFactory->create();
            return $resultRedirect->setPath('advencedimport/listing/index/');
        }
    }

    protected function getSkippedCsv(int $entityId)
    {
        $modelImport = $this->importRepository->getById($entityId);
        $csvFile = $this->helperData->getSkippedCsvReadFile($modelImport->getIdentifier());
        return $csvFile->readAll();
    }
}
